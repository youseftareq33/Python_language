'''
Dictionary Built-in Methods:
dict.get(key, default): Returns the value for a key if it exists, otherwise returns the default value.

dict.keys(): Returns a view of the dictionary's keys.

dict.values(): Returns a view of the dictionary's values.

dict.items(): Returns a view of the dictionary's key-value pairs as tuples.

dict.pop(key, default): Removes and returns the value for the given key. If the key doesn't exist, returns the default value.

dict.update(other_dict): Updates the dictionary with key-value pairs from another dictionary.

dict.clear(): Removes all items from the dictionary.
#----------------------------------------------------------------------------------------------------------

List Built-in Methods:
list.append(item): Adds an item to the end of the list.

list.extend(iterable): Appends elements from an iterable to the end of the list.

list.insert(index, item): Inserts an item at a specific index.

list.remove(item): Removes the first occurrence of a value from the list.

list.pop(index): Removes and returns the item at the specified index.

list.index(item, start, end): Returns the index of the first occurrence of an item within the specified range.

list.count(item): Returns the number of occurrences of an item in the list.

list.sort(key=None, reverse=False): Sorts the list in place.
#----------------------------------------------------------------------------------------------------------

Set Built-in Methods:
set.add(item): Adds an item to the set.

set.update(iterable): Adds items from an iterable to the set.

set.remove(item): Removes the specified item from the set. Raises an error if the item doesn't exist.

set.discard(item): Removes the specified item from the set, if it exists. Does nothing if the item is not present.

set.pop(): Removes and returns an arbitrary element from the set.
#----------------------------------------------------------------------------------------------------------

String Built-in Methods:
str.upper(): Returns a copy of the string with all characters in uppercase.

str.lower(): Returns a copy of the string with all characters in lowercase.

str.strip(): Returns a copy of the string with leading and trailing whitespace removed.

str.split(separator): Splits the string into a list of substrings based on the specified separator.

str.join(iterable): Joins elements of an iterable using the string as a separator.

str.replace(old, new): Returns a copy of the string with occurrences of 'old' replaced by 'new'.

str.startswith(prefix): Checks if the string starts with the specified prefix.

str.endswith(suffix): Checks if the string ends with the specified suffix.
'''